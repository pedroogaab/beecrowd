a, b, c = input().split()

a, b, c = float(a), float(b), float(c)

print(f"TRIANGULO: {(c*a)/2:.3f}") # area do traingulo retangulo = (catAd*catOp)/2 >> multilplicação dos catetos e dividido por 2
print(f"CIRCULO: {3.14159*(c**2):.3f}") # area = pi*r² >> pi vezes o raio ao quadrado
print(f"TRAPEZIO: {(a+b)*(c/2):.3f}") # area = (a+b)h/2 >> bases vezes a altura dividido por dois
print(f"QUADRADO: {b**2:.3f}") # area = a² >> lado*lado 
print(f"RETANGULO: {a*b:.3f}") #area = a*b >> largura vezes altura 